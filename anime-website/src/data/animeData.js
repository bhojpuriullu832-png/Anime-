export const getAnimeById = (id) => {
  return animeData.find(anime => anime.id === parseInt(id));
};

export const getAnimeByGenre = (genre) => {
  if (genre === "All") return animeData;
  return animeData.filter(anime => 
    anime.genres.some(g => g.toLowerCase() === genre.toLowerCase())
  );
};

export const getFeaturedAnime = () => {
  return animeData.filter(anime => anime.featured);
};

export const getTrendingAnime = () => {
  return animeData.filter(anime => anime.trending);
};

export const getNewReleases = () => {
  return animeData.filter(anime => anime.newRelease);
};

export const searchAnime = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return animeData.filter(anime => 
    anime.title.toLowerCase().includes(lowercaseQuery) ||
    anime.originalTitle.toLowerCase().includes(lowercaseQuery) ||
    anime.genres.some(genre => genre.toLowerCase().includes(lowercaseQuery))
  );
};